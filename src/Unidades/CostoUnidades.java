public class CostoUnidades {
    public int costosolados;
    public int costovehiculos;
    public int costoestructura;

    public CostoUnidades() {
    }

    public int getCostosolados() {
        return costosolados;
    }

    public void setCostosolados(int costosolados) {
        this.costosolados = costosolados;
    }

    public int getCostovehiculos() {
        return costovehiculos;
    }

    public void setCostovehiculos(int costovehiculos) {
        this.costovehiculos = costovehiculos;
    }

    public int getCostoestructura() {
        return costoestructura;
    }

    public void setCostoestructura(int costoestructura) {
        this.costoestructura = costoestructura;
    }
    
}
